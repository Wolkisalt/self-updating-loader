std::string GetHWID() {
    std::stringstream ss;
    
    // Get CPU Info
    int cpuinfo[4] = { 0, 0, 0, 0 };
    __cpuid(cpuinfo, 1);
    ss << std::hex << cpuinfo[0] << cpuinfo[3];
    
    // Get Volume Serial Number
    DWORD volSerial = 0;
    GetVolumeInformationA("C:\\", NULL, 0, &volSerial, NULL, NULL, NULL, 0);
    ss << "-" << std::hex << volSerial;
    
    return ss.str();
}

void CopyToClipboard(const std::string& text) {
    if (OpenClipboard(NULL)) {
        EmptyClipboard();
        HGLOBAL hg = GlobalAlloc(GMEM_MOVEABLE, text.size() + 1);
        if (hg) {
            memcpy(GlobalLock(hg), text.c_str(), text.size() + 1);
            GlobalUnlock(hg);
            SetClipboardData(CF_TEXT, hg);
        }
        CloseClipboard();
    }
}

bool VerifyLicense(const std::string& hwid, const std::string& base64_signature) {
    if (base64_signature.empty()) return false;

    // Decode Base64 Signature
    DWORD sigLen = 0;
    if (!CryptStringToBinaryA(base64_signature.c_str(), 0, CRYPT_STRING_BASE64, NULL, &sigLen, NULL, NULL)) return false;
    std::vector<BYTE> signature(sigLen);
    if (!CryptStringToBinaryA(base64_signature.c_str(), 0, CRYPT_STRING_BASE64, signature.data(), &sigLen, NULL, NULL)) return false;

    BCRYPT_ALG_HANDLE hAlg = NULL;
    if (BCryptOpenAlgorithmProvider(&hAlg, BCRYPT_RSA_ALGORITHM, NULL, 0) != 0) return false;

    BCRYPT_KEY_HANDLE hKey = NULL;
    if (BCryptImportKeyPair(hAlg, NULL, BCRYPT_RSAPUBLIC_BLOB, &hKey, (PUCHAR)RSA_PUBLIC_KEY, sizeof(RSA_PUBLIC_KEY), 0) != 0) {
        BCryptCloseAlgorithmProvider(hAlg, 0);
        return false;
    }

    // Hash the HWID with SHA256
    BCRYPT_ALG_HANDLE hHashAlg = NULL;
    BCryptOpenAlgorithmProvider(&hHashAlg, BCRYPT_SHA256_ALGORITHM, NULL, 0);
    
    DWORD hashLen = 0, resLen = 0;
    BCryptGetProperty(hHashAlg, BCRYPT_HASH_LENGTH, (PUCHAR)&hashLen, sizeof(DWORD), &resLen, 0);
    std::vector<BYTE> hash(hashLen);
    BCRYPT_HASH_HANDLE hHash = NULL;
    BCryptCreateHash(hHashAlg, &hHash, NULL, 0, NULL, 0, 0);
    BCryptHashData(hHash, (PUCHAR)hwid.c_str(), hwid.size(), 0);
    BCryptFinishHash(hHash, hash.data(), hashLen, 0);
    BCryptDestroyHash(hHash);

    // Verify Signature (PKCS1 padding)
    BCRYPT_PKCS1_PADDING_INFO paddingInfo = { BCRYPT_SHA256_ALGORITHM };
    NTSTATUS status = BCryptVerifySignature(hKey, &paddingInfo, hash.data(), hashLen, signature.data(), signature.size(), BCRYPT_PAD_PKCS1);

    BCryptCloseAlgorithmProvider(hHashAlg, 0);
    BCryptDestroyKey(hKey);
    BCryptCloseAlgorithmProvider(hAlg, 0);

    return (status == 0); // 0 is STATUS_SUCCESS
}

bool SaveLicense(const std::string& signature) {
    std::ofstream file("license.dat");
    if (file.is_open()) {
        file << signature;
        file.close();
        return true;
    }
    return false;
}

std::string LoadLicense() {
    std::ifstream file("license.dat");
    std::string sig;
    if (file.is_open()) {
        file >> sig;
        file.close();
    }
    return sig;
}
